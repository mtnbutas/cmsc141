window.addEventListener("keydown", function(e){
		if(e.keyCode == 37){//left
			if(maze[0][0].x < - 10){
				for(i = 0; i < maze.length; i+=1){
					var rows = maze[i];
					for(j = 0; j < rows.length; j+=1){
						rows[j].x += 10;
					}
				}
				if(user.x!=window.innerWidth/2-25){
					user.x -= 2;
				}
			}
			else{
				user.x -= 10;
			}
			updateWorld();
		}
		if(e.keyCode == 38){//up
			if(maze[0][0].y < 0){
				for(i = 0; i < maze.length; i+=1){
					var rows = maze[i];
					for(j = 0; j < rows.length; j+=1){
						rows[j].y += 10;
					}
				}
				if(user.y!=window.innerHeight/2-25){
					user.y -= 2;
				}
			}
			else{
				user.y -= 10;
			}
			updateWorld();		
		}
		if(e.keyCode == 39){//right
			if(maze[0][maze[0].length-1].x > canvas.width - tilewidth + 10){
				for(i = 0; i < maze.length; i+=1){
					var rows = maze[i];
					for(j = 0; j < rows.length; j+=1){
						rows[j].x -= 10;
					}
				}
				if(user.x!=window.innerWidth/2-25){
					user.x += 2;
				}
			}
			else{
				user.x += 10;
			}

			updateWorld();
			
		}
		if(e.keyCode == 40){//down
			if(maze[maze.length-1][0].y > canvas.height - tileheight + 15){
				for(i = 0; i < maze.length; i+=1){
					var rows = maze[i];
					for(j = 0; j < rows.length; j+=1){
						rows[j].y -= 10;
					}
				}
				if(user.y!=window.innerHeight/2-25){
					user.y += 2;
				}
			}
			else{
				user.y += 10;
			}
			updateWorld();
		}
}, false);
